def norm(s: str) -> str:
    s = s.lower()
    s = re.sub(r"[^\w\s]", " ", s, flags=re.U)
    return re.sub(r"\s+", " ", s).strip()


def best_id(pool: list, keywords: list[str], min_score: int = 2) -> int | None:
    words = [w for w in norm(" ".join(keywords)).split() if len(w) > 3]
    best = (0, None)
    for item in pool:
        t = norm(item.get("question", ""))
        score = sum(1 for w in words if w in t)
        if norm(" ".join(keywords))[:40] in t:
            score += 4
        if score > best[0]:
            best = (score, int(item["id"]))
    return best[1] if best[0] >= min_score else None


