def format_question(text: str) -> str:
    t = text.strip()
    if not t.startswith("**"):
        t = f"**{t}**"
    return t


def base_entry(num: int, ped_text: str, patch: dict, stom_by_id: dict) -> dict:
    stom_id = patch.get("stom_id")
    stom = stom_by_id.get(stom_id) if stom_id else None
    answer = patch.get("answer") or (stom["answer"] if stom else "")
    key_facts = patch.get("key_facts") or (stom.get("key_facts") if stom else [])
    game_modes = stom.get("game_modes", ["flashcard", "quiz"]) if stom else ["flashcard", "quiz"]

    if num == 60 and stom:
        # Trim переносчики section — not in ped question
        answer = answer.split("\n\n**Переносчики**")[0].strip()
        # Append host classification from stom 68
        host_block = stom_by_id[68]["answer"].split("**Инвазия**")[0].strip()
        answer = answer + "\n\n" + host_block

    return {
        "id": num,
        "question": format_question(ped_text),
        "answer": answer,
        "topic": patch["topic"],
        "subtopic": patch["subtopic"],
        "difficulty": patch["difficulty"],
        "game_modes": game_modes,
        "key_facts": key_facts,
        "repeat_interval": stom.get("repeat_interval", 4) if stom else 4,
        "exam_weight": stom.get("exam_weight", 3) if stom else 3,
        "faculty": ["pediatrics"],
        "subject": "biology",
        "visible": True,
        "premium": True,
        "source_stom_ids": patch["source_stom_ids"],
        "match_ratio": 1.0,
    }


def main() -> None:
    ped = json.loads(PED.read_text(encoding="utf-8"))
    stom = json.loads((ROOT / "src" / "data" / "bio_questions_stomatology.json").read_text(encoding="utf-8"))
    gloss = json.loads(GLOSS.read_text(encoding="utf-8"))
    stom_by_id = {int(x["id"]): x for x in stom}

    by_id = {int(x["id"]): x for x in ped}

    # Fix Q32
    if 32 in by_id:
        by_id[32]["answer"] = Q32_ANSWER
        by_id[32].update(Q32_META)

    # Add Q56-61
    for patch in NEW_QUESTIONS:
        num = patch["num"]
        entry = base_entry(num, patch["text"], patch, stom_by_id)
        if num in by_id:
            by_id[num].update(entry)
        else:
            ped.append(entry)

    ped.sort(key=lambda x: int(x["id"]))
    PED.write_text(json.dumps(ped, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")

    # Update ped list for scripts
    if PED_LIST.exists():
        ped_list = json.loads(PED_LIST.read_text(encoding="utf-8"))
        existing_nums = {int(x["num"]) for x in ped_list}
        for patch in NEW_QUESTIONS:
            if int(patch["num"]) not in existing_nums:
                ped_list.append({"num": patch["num"], "text": patch["text"]})
        ped_list.sort(key=lambda x: int(x["num"]))
        PED_LIST.write_text(json.dumps(ped_list, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")

    existing = {g["term"].lower() for g in gloss}
    added = 0
    for entry in NEW_GLOSSARY:
        if entry["term"].lower() in existing:
            continue
        gloss.append(entry)
        existing.add(entry["term"].lower())
        added += 1
    GLOSS.write_text(json.dumps(gloss, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")

    print(f"pediatrics questions: {len(ped)} (ids 1-{max(int(x['id']) for x in ped)})")
    print(f"glossary: +{added} terms")


if __name__ == "__main__":
    main()
